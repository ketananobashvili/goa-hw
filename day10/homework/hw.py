#2) მოიყვანეთ while loop-ის მაგალითი და კომენტარის სახით ახსენით, თუ როგორ მუშაობს while ციკლი;

i = 1 #საიტერაციო ცვლადს აქვს მნიშვნელობა 1, ანუ თვლა დაიწყება ერთიდან

while 10 > i: #სანამ 10 მეტია საიტერაციო ცვლადზე
    print(i) #დაიპრინტება საიტერაციო ცვლადი
    i = i + 1 #საიტერაციო ცვლადი იზრდება 1 ით